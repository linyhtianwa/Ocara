namespace Ocara\Core;

use Ocara\Core\ServiceProvider;

defined('OC_PATH') or exit('Forbidden!');

class Session extends ServiceProvider
{
	public function register()
    {
        parent::register(); // TODO: Change the autogenerated stub

        $class = ocConfig('SESSION.handler', 'Ocara\Sessions\SessionFile');
        $this->_container->bindSingleton('sessionHandler', function() use ($class) {
            $handler = new $class();
            return $handler;
        });
    }

    /**
	 * Session初始化处理
	 * @param $start
	 */
	public function boot($start = true)
	{
        $handler = $this->sessionHandler;

        if ($handler){
            session_set_save_handler(
                array(&$handler, 'open'),
                array(&$handler, 'close'),
                array(&$handler, 'read'),
                array(&$handler, 'write'),
                array(&$handler, 'destroy'),
                array(&$handler, 'gc')
            );
            register_shutdown_function('session_write_close');
        }

		$this->_start($start);
	}

	/**
	 * 启动Session
	 * @param bool $start
	 */
	private function _start($start)
	{
		$saveTime = intval(ocConfig('SESSION.options.save_time', false));

		if ($saveTime) {
			$this->setSaveTime($saveTime);
		}

		if ($start && !isset($_SESSION)) {
			if (!headers_sent()) {
				session_start();
			}
		}
		
		if ($saveTime) {
			$this->setCookie($saveTime);
		}
	}

    /**
     * 获取session变量值
     * @param bool $name
     * @return array|bool|mixed|null
     */
    public function get($name = null)
    {
        if (isset($name)) {
            return ocGet($name, $_SESSION);
        }

        return $_SESSION;
    }

	/**
	 * 设置session变量
	 * @param string|array $name
	 * @param mixed $value
	 */
	public function set($name, $value)
	{
		ocSet($_SESSION, $name, $value);
	}

	/**
	 * 删除session变量
	 * @param string|array $key
	 */
	public function delete($name)
	{
		ocDel($_SESSION, $name);
	}

	/**
	 * 获取session ID
	 */
	public function getId()
	{
		return session_id();
	}

	/**
	 * 获取session Name
	 */
	public function getName()
	{
		return session_name();
	}

    /**
     * 清空session数组
     * @param null $args
     */
	public function clear($args = null)
	{
		session_unset();
	}

	/**
	 * > PHP7 回收session
	 */
	public function gc()
	{
		session_gc();
	}

	/**
	 * 检测session是否设置
	 * @param $key
	 * @return array|bool|mixed|null
	 */
	public function has($name)
	{
		return ocKeyExists($name, $_SESSION);
	}

	/**
	 * 释放session，删除session文件
	 */
	public function destroy()
	{
		if (session_id()) {
			return session_destroy();
		}
	}

	/**
	 * cookie保存session设置
	 * @param $saveTime
	 * @param string $path
	 * @param string $domain
	 * @param bool $secure
	 * @param bool $httponly
	 */
	public function setCookie($saveTime, $path = null, $domain = false, $secure = false, $httponly = true)
	{	
		if (session_id()) {
			ocService()->cookie->create(
			    session_name(),
                session_id(),
                $saveTime,
                $path,
                $domain,
                $secure,
                $httponly
            );
		}
	}

	/**
	 * 设置session有效期(单位为秒)
	 * @param integer $saveTime
	 * @return string
	 */
	public function setSaveTime($saveTime)
	{
		return @ini_set('session.gc_maxlifetime', $saveTime);
	}

	/**
	 * 序列化session数组
	 */
	public function serialize()
	{
		return session_encode();
	}

	/**
	 * 反序列化session串
	 * @param string $data
	 * @return bool
	 */
	public function unserialize($data)
	{
		return session_decode($data);
	}
}
